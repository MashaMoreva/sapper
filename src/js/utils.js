export function convert(counter) {
    if (counter === 1) {
        return 'one'
    }
    if (counter === 2) {
        return 'two'
    }
    if (counter === 3) {
        return 'three'
    }
    if (counter === 4) {
        return 'four'
    }
    if (counter === 5) {
        return 'five'
    }
    if (counter === 6) {
        return 'six'
    }
    if (counter === 7) {
        return 'seven'
    }
    if (counter === 8) {
        return 'eight'
    }
}